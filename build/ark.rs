fn parse_hex(digit: u8) -> u8 {
    match digit {
        b'0'..=b'9' => digit - b'0',
        b'a'..=b'f' => digit - b'a' + 10,
        b'A'..=b'F' => digit - b'A' + 10,
        _ => 0,
    }
}

fn uint256(buff: &[u8]) -> [u8; 32] {
    let mut result: [u8; 32] = [0; 32];

    for i in 0..32 {
        result[31 - i] = (parse_hex(buff[(i << 1)]) << 4) | (parse_hex(buff[(i << 1) + 1]));
    }

    result
}

pub(crate) fn write_to(filename: &str) -> std::io::Result<()> {
    let mut buf: Vec<u8> = vec![];

    for i in 0..ARK_BYTES.len() {
        buf.extend_from_slice(&uint256(&ARK_BYTES[i][..]));
    }

    let mut file = File::create(filename)?;
    file.write_all(&buf)?;
    Ok(())
}
